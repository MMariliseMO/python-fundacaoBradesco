#calcular a média do aluno. Notas >= 7.00, aluno aprovado. Nota < 7.00, aluno reprovado. Tem que aparecer se ele foi aprovado ou reprovado e o valor da média

nota1 = float(input("Informe a 1º nota: "))
nota2 = float(input("Informe a 2º nota: "))

#cálculo da média
media = (nota1 + nota2) / 2

#verificar a condição do aluno
if media >= 7:
    print("Aluno aprovado com média %.1f" % media) #usa o %1.f para %indicar uma sequência de formação, .1indica uma casa decimal e o 'f' quer dizer que é de um ponto flutuante float
else:
    print("Aluno reprovado com média %.1f " % media)


































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































